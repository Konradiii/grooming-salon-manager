const API_ERROR_PATTERNS = [
  { regex: /^Owner does not exist\.?$/, key: 'error.ownerNotExist' },
  { regex: /^Dog does not exist\.?$/, key: 'error.dogNotExist' },
  { regex: /^Owner with id (\d+) was not found\.?$/, key: 'error.ownerNotFound' },
  { regex: /^Dog with id (\d+) was not found\.?$/, key: 'error.dogNotFound' },
  { regex: /^Appointment with id (\d+) was not found\.?$/, key: 'error.appointmentNotFound' },
  { regex: /^Invalid username or password\.?$/, key: 'login.error' },
];

export function translateApiError(message, language) {
  if (!message) return message;
  for (const { regex, key } of API_ERROR_PATTERNS) {
    const match = message.match(regex);
    if (match) {
      const entry = translations[language]?.[key] ?? translations.en[key];
      return typeof entry === 'function' ? entry(match[1]) : entry;
    }
  }
  return message;
}
